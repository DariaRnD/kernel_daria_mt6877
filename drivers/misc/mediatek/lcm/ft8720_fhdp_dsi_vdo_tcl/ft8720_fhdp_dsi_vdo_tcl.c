static unsigned int lcm_ata_check(unsigned char *bufferr)
{
	unsigned int ret = 0;
	unsigned int id[4] = {0x02, 0x3C, 0xb0, 0x0A};
	unsigned int data_array[4];
	unsigned char read_buf[4];

	data_array[0] = 0x00043700; /* set max return size = 3 */
	dsi_set_cmdq(data_array, 1, 1);

	read_reg_v2(0xbf, read_buf, 4); /* read lcm id */

	LCM_LOGI("ATA read = 0x%x, 0x%x, 0x%x, 0x%x\n",
		 read_buf[0], read_buf[1], read_buf[2], read_buf[3]);

	if ((read_buf[0] == id[0]) &&
	    (read_buf[1] == id[1]) &&
	    (read_buf[2] == id[2]) &&
	    (read_buf[3] == id[3]))
		ret = 1;
	else
		ret = 0;

	return ret;
}
static void lcm_init(void)
{
	display_bias_enable();
	MDELAY(10);

	SET_RESET_PIN(1);
	MDELAY(10);
	SET_RESET_PIN(0);
	MDELAY(10);

	SET_RESET_PIN(1);
	MDELAY(60);

	push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting)/sizeof(struct LCM_setting_table), 1);
}

static void lcm_suspend(void)
{
	push_table(lcm_suspend_setting,sizeof(lcm_suspend_setting) / sizeof(struct LCM_setting_table), 1);
	display_bias_disable();
	MDELAY(10);
	SET_RESET_PIN(0);
	MDELAY(10);
}

static void lcm_resume(void)
{
	lcm_init();
}

static void lcm_init_power(void)
{
	display_bias_enable();
}

static void lcm_suspend_power(void)
{
	display_bias_disable();
}

static void lcm_resume_power(void)
{
	SET_RESET_PIN(0);
	display_bias_enable();
}
